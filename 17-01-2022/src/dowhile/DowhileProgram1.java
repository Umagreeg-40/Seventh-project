package dowhile;

public class DowhileProgram1 
{

	public static void main(String[] args)
	{
		// do-while loop
		
		/*
		 *  initialization;
		 *  
		 *  do
		 *  {
		 *  	// print statement;
		 *  	// increment/decrement statement;
		 *  
		 */
		
		int a = 1;
		
		do
		{
			System.out.println(a);
			a++;
		}while(a<=5);

	}

}
