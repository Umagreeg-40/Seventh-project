package whileLoop;

public class WhileLoopProgram1 
{

	public static void main(String[] args)
	{
		
		/*  while loop
		 * 
		 * initialization;
		 * 
		 * while(condition)
		 * {
		 * 
		 *  // print statement
		 *  // increment/decrement statement
		 *
		 */
		
		
		int a=1;
		
		while(a<=5)
		{
			System.out.println(a);
			a++;
		}

	}

}
